.text
.global INC_SAT_V
.type INC_SAT_V ,"function"

INC_SAT_V:	DUP V1.4S, W2 //COMO SAO INTS 128/32=4 -> 4S
WHILE:		CBZ W1, FIM
			SUB W1, W1, 4 //VAMOS TRABALHAR COM 4 ELEMENTOS DE CADA VEZ
			LDR Q0, [X0] // CONSIDERAMOS QUE SAO 4 (4*4=16)ELEMENTOS DE CADA VEZ A SER PROCESSADOS
			SQADD V0.4S, V0.4S, V1.4S
			STR Q0, [X0], 16 //GUARDAR NO INICIO DO VETOR e depois somar 4 enderecos
			B WHILE

FIM:		RET
