.text
.global NORM_V
.type NORM_V ,"function"

NORM_V:	FMOV D9, XZR
WHILE:	CBZ X1, FIM
		SUB X1, X1, 2
		LDR Q0, [X0], 16 //CARREGA 2 DOUBLES (2*8=16)
		FMUL V0.2D, V0.2D, V0.2D //OS VALORES PASSAM A ESTAR AO QUADRADO
		FADDP D0, V0.2D // SOMA OS 2 VALORES ENTRE ELES
		FADD D9, D9, D0 //SOMA COM O RESULTADO ANTERIOR
		B WHILE


FIM:	FSQRT D0, D9
		RET
